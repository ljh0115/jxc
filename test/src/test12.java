/*
 * switch类型
 */
public class test12 {
	public static void main(String[] args){
		int a=12;
		switch(a){
		case 1:System.out.println(a+"月是春季");
		case 2:System.out.println(a+"月是春季");
		case 3:System.out.println(a+"月是春季");break;
		case 4:System.out.println(a+"月是夏季");
		case 5:System.out.println(a+"月是夏季");
		case 6:System.out.println(a+"月是夏季");break;
		case 7:System.out.println(a+"月是秋季");
		case 8:System.out.println(a+"月是秋季");
		case 9:System.out.println(a+"月是秋季");break;
		case 10:System.out.println(a+"月是冬季");
		case 11:System.out.println(a+"月是冬季");
		case 12:System.out.println(a+"月是冬季");break;
		default:System.out.println("月份错误");
		}
	}
}
